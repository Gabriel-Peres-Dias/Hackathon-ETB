package com.stefanini.cursojavabasico.operadores;

public class OperadorAtribuicao {
	public static void main(String[] args) {
		// =
		
		int numero = 10;
		
		// Aditivo 
		
		numero += 10;
		numero = numero + 10; // Mesma coisa*
		
		// Subtrativo
		
		numero -= 10;
		
		// Multiplicativo
		
		numero *= 10;
		
		// Divisivo
		
		numero /= 10;
		
		// Mudular
		
		numero %= 2;
	}

}
